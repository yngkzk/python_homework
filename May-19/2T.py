# 2. Запросите у пользователя 2 числа и выведите среднее арифметическое этих чисел.
users_numb = input("Enter the first number: ")
users_numb2 = input("Enter the second number: ")
number = int(users_numb)
number2 = int(users_numb2)
result = (number + number2) / 2
print("Your average number is: ", result)
